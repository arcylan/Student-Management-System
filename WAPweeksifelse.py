days = int(input("Enter a Number 1-7  >> "))
if days==1:
    print("SUNDAY")
elif days==2:
    print("MONDAY")
elif days==3:
    print("TUESDAY")
elif days==4:
    print("WEDSDAY")
elif days==5:
    print("THURSDAY")
elif days==6:
    print("FRIDAY")
else:
    print("SATURDAY")                        